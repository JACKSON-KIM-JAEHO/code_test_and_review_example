def add(a, b):
    """Add two numbers."""
    return a + b

def minus(a, b):
    """Subtract b from a."""
    return a - b

def multiply(a, b):
    """Multiply two numbers."""
    return a * b
    
def divide(a, b):
    if b ==0:
        return '0으로 나눌수 없음.'
    return a / b
def make0(a):
    return a*0
